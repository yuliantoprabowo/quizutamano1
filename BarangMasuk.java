package quizutamano1;

public class BarangMasuk {
    Integer modalAwal=20000000;
    String jenisBarang,namaBarang,jumlah;
    Integer harga;

    String nama,nik;

    public void setModalAwal(Integer modalAwal) {
        this.modalAwal = modalAwal;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getNik() {
        return nik;
    }

    public void setNik(String nik) {
        this.nik = nik;
    }

    public Integer getModalAwal() {
        return modalAwal;
    }

    public String getJenisBarang() {
        return jenisBarang;
    }

    public void setJenisBarang(String jenisBarang) {
        this.jenisBarang = jenisBarang;
    }

    public String getNamaBarang() {
        return namaBarang;
    }

    public void setNamaBarang(String namBarang) {
        this.namaBarang = namBarang;
    }

    public String getJumlah() {
        return jumlah;
    }

    public void setJumlah(String jumlah) {
        this.jumlah = jumlah;
    }

    public Integer getHarga() {
        return harga;
    }

    public void setHarga(Integer harga) {
        this.harga = harga;
    }  
}
